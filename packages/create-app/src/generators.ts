import type { ProviderSelections } from "./providers.js";
import { collectEnvKeys } from "./providers.js";

// ─── next.config.js ───────────────────────────────────────────────────────────

export function generateNextConfig(): string {
  return `/** @type {import('next').NextConfig} */
const { withFeatures } = require("@mohasinac/cli/next");
const features = require("./features.config").default;

module.exports = withFeatures(features)({
  serverExternalPackages: ["firebase-admin"],
  images: {
    remotePatterns: [
      { hostname: "firebasestorage.googleapis.com" },
      { hostname: "storage.googleapis.com" },
    ],
  },
});
`;
}

// ─── i18n/request.ts ──────────────────────────────────────────────────────────

export function generateI18nRequest(locales: string[]): string {
  const defaultLocale = locales[0] ?? "en";
  return `import { getRequestConfig } from "next-intl/server";
import { mergeFeatureMessages } from "@mohasinac/cli/i18n";
import features from "../../features.config";

export default getRequestConfig(async ({ locale }) => {
  const base = (await import(\`../../messages/\${locale}.json\`)).default;
  const feature = await mergeFeatureMessages(locale, features);
  return {
    locale,
    messages: { ...feature, ...base },
  };
});
`;
}

// ─── middleware.ts ────────────────────────────────────────────────────────────

export function generateMiddleware(locales: string[], auth: ProviderSelections["auth"]): string {
  const localeList = JSON.stringify(locales);
  const defaultLocale = locales[0] ?? "en";

  if (auth === "firebase") {
    return `import { createMiddlewareAuthChain } from "@mohasinac/auth-firebase";
import createIntlMiddleware from "next-intl/middleware";
import { NextRequest } from "next/server";

const intlMiddleware = createIntlMiddleware({
  locales: ${localeList},
  defaultLocale: "${defaultLocale}",
});

const authMiddleware = createMiddlewareAuthChain({
  protectedPaths: ["/account", "/checkout", "/orders", "/seller", "/admin"],
  adminPaths: ["/admin"],
});

export async function middleware(request: NextRequest) {
  const intlResult = intlMiddleware(request);
  if (!intlResult.headers.get("x-middleware-next")) return intlResult;
  return authMiddleware(request);
}

export const config = {
  matcher: ["/((?!api|_next|.*\\\\..*).*)"],
};
`;
  }

  // nextauth / clerk — simpler
  return `import createIntlMiddleware from "next-intl/middleware";
import { NextRequest } from "next/server";

const intlMiddleware = createIntlMiddleware({
  locales: ${localeList},
  defaultLocale: "${defaultLocale}",
});

export function middleware(request: NextRequest) {
  return intlMiddleware(request);
}

export const config = {
  matcher: ["/((?!api|_next|.*\\\\..*).*)"],
};
`;
}

