// ─── .env.example ─────────────────────────────────────────────────────────────

export function generateEnvExample(selections: ProviderSelections): string {
  const keys = collectEnvKeys(selections);
  const lines = keys.map((k) => `${k}=`);
  return `# Generated by @mohasinac/create-app
# Fill in the values for your chosen providers.

${lines.join("\n")}

# Optional — uncomment as needed
# NEXT_PUBLIC_BASE_URL=http://localhost:3000
# RATE_LIMIT_UPSTASH_URL=
# RATE_LIMIT_UPSTASH_TOKEN=
`;
}

// ─── app/layout.tsx ───────────────────────────────────────────────────────────

export function generateRootLayout(projectName: string, locales: string[]): string {
  const defaultLocale = locales[0] ?? "en";
  return `import type { Metadata } from "next";
import { NextIntlClientProvider } from "next-intl";
import { getMessages } from "next-intl/server";
import "@/providers.config";

export const metadata: Metadata = {
  title: "${projectName}",
};

export default async function RootLayout({
  children,
  params,
}: {
  children: React.ReactNode;
  params: { locale: string };
}) {
  const messages = await getMessages();
  return (
    <html lang={params.locale}>
      <body>
        <NextIntlClientProvider messages={messages}>
          {children}
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
`;
}
